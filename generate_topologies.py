def main():
    """Generate all topology files"""
    import os
    
    # Create topologies directory if it doesn't exist
    os.makedirs('topologies', exist_ok=True)
    
    # Process glycine
    print("Processing structures/glycine_zw.pdb...")
    glycine_atoms = parse_pdb('structures/glycine_zw.pdb')
    write_gro_file(glycine_atoms, 'topologies/glycine_charmm.gro', 'Glycine CHARMM36m')
    write_topology_charmm36m(glycine_atoms, 'topologies/glycine_charmm.top', 'GLY')
